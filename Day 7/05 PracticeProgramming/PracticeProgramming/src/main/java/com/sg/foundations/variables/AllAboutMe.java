package com.sg.foundations.variables;

public class AllAboutMe {
    public static void main(String[] args) {
        // Declare variables
        String name, favouriteFood, residenceType;
        int numberOfPets;
        boolean learnedToWhistle;
        
        // Populate variables
        name = "Asel Perera";
        favouriteFood = "fried chicken";
        numberOfPets = 4;
        residenceType = "house";
        learnedToWhistle = true;
        
        // Print
        System.out.println("My name is " + name + ".");
        System.out.println("My favourite food is " + favouriteFood + ".");
        System.out.println("I have " + numberOfPets + " pets.");
        System.out.println("I live in a " + residenceType + ", and I love it here.");
        System.out.println("It is " + learnedToWhistle + " I know how to whistle.");
    }
}
